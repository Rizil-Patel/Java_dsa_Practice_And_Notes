public class first01 {
    public static void main(String[] args){
        System.out.println("The And bitwise is:"+(5 & 6));
        System.out.println("The Or bitwise is:"+(5 | 6));
        System.out.println("The Xor bitwise is:"+(5 ^ 6));
        System.out.println("The 1's complement bitwise is:"+(~5));
        System.out.println("The 1's complement bitwise is:"+(~0));
        System.out.println("The left shift:"+(5<<2));
        System.out.println("The right shift:"+(6>>1));

    }
}
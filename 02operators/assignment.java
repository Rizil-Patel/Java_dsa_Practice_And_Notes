public class assignment {
    public static void main(String[] args){
        // this type of assignment gives faster execution
        int a = 10;
        a += 10;//a = a+10
        System.out.println(a);
        a -= 10;// a = a - 10
        System.out.println(a);
        a *= 10;// a = a* 10
        System.out.println(a);
        a /= 10;// a= a/10
        System.out.println(a);
        a %= 10;//a= a/10
        System.out.println(a);
    }
}



public class Arithmatic {
    public static void main(String[] args){
        // Binary operators
        int a = 3;
        int b = 8;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(b%a);
    }
}

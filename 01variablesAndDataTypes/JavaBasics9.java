import java.util.*;
public class JavaBasics9 {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        // this is called explicit conversion type casting or narrowing the conversion
        float num = 20.94f;
        int a = (int)num;
        System.out.println(a);
        char ch = 'a';
        int num1 = ch;
        System.out.println(num1);
    }
}

